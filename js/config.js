/**
 * config.js — 調整パラメータ集約
 *
 * ゲームの「体感」を決める数値はすべてここに集める。
 * チューニングするときにロジックのファイルを触らなくて済むようにするのが目的。
 */

/** 掛け声・進行のタイミング(ミリ秒) */
export const TIMING = {
  CHANT_BEAT: 650,     // 「じゃん」「けん」の1拍
  JUDGE_TIMEOUT: 2000, // 「ぽん!」後、手が読めるまで待つ上限
  RESULT_MS: 1800,     // 結果(判定写真)の表示時間
  RETRY_MS: 1200,      // 手が読めなかったときの仕切り直しまでの時間

  // 当機が出した直後の「手替え」の扱いは時間で二段階に分ける:
  //
  //   0 〜 REJUDGE_WINDOW ……… 境界ギリギリの投げ遅れとみなし、反則ではなく
  //                            当機が判定をやり直す(判定写真(改)。当機は勝つ)
  //   〜 FOUL_WINDOW ………… 当機の手を見てから変えたとみなし、反則負け
  //   FOUL_WINDOW 以降 ……… 何もしない(手を下ろす動作を冤罪にしない)
  //
  // 人間が「当機の手を見て→変えよう と決めて→動かす」には最短でも
  // 0.4〜0.6秒かかるので、0.5秒より前に届いた手替えは「元々それを
  // 出そうとしていた」と推定できる、というのが猶予の根拠。
  REJUDGE_WINDOW: 500,
  FOUL_WINDOW: 1200,

  COUNT_BEAT: 700,     // 次の勝負までのカウントダウン1拍
};

/** カウントダウンの開始数字(3・2・1) */
export const COUNT_FROM = 3;

/**
 * フレーム数しきい値
 *
 * カメラは約30fpsなので「3フレーム=約100ms」。
 * すべて「誤認識のチラつき」と「反応の速さ」のトレードオフで決まる:
 * 増やすほど誤動作は減るが、後出しが遅くなって人間にバレる。
 */
export const FRAMES = {
  STABLE: 3,   // 同じ手がこのフレーム数連続したら「出した」と確定
  FOUL: 5,     // 結果表示中の手替え(反則)は少し厳しめに確定(冤罪防止)
  PRESENT: 10, // 手がこのフレーム数連続で見えたら勝負開始
  MISS: 8,     // このフレーム数連続で見失ったら「手を引っ込めた」とみなす

  // 「ぽん!」の瞬間に見えていた手(=じゃん・けんで振っていた拳)と同じ手を
  // 確定させるのに必要な連続フレーム数。振りの残りのグーを 3 フレームで
  // 即確定すると、本当の手が届く前に勝負が決まり「境界ギリギリの人」が
  // 毎回後出し扱いになる。長め(約0.4秒)に粘って本当の手を待つ。
  CARRY: 12,
};

/**
 * 指の伸展判定のマージン。
 * 指先が第二関節より「この倍率以上」手首から遠ければ伸びているとみなす。
 * 1.0 に近づけるほど敏感になるが、半端に曲げた指を拾ってしまう。
 */
export const EXTENSION_MARGIN = 1.1;

/** MediaPipe Hands の設定。後出しは速さが正義なので軽量モデル(complexity 0)を使う */
export const HANDS_OPTIONS = {
  maxNumHands: 1,
  modelComplexity: 0,
  minDetectionConfidence: 0.6,
  minTrackingConfidence: 0.5,
};

/** カメラ解像度。上げると認識は安定するが推論が遅くなる */
export const CAMERA_SIZE = { width: 640, height: 480 };
